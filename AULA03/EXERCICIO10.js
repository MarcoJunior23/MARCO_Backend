// Declarando variáveis de idade mínima e idade do usuário
let idadeMinima = 18;
let idadeUsuario = 20; // exemplo de idade do usuário

// Usando estruturas condicionais (if, else) para verificar se o usuário pode comprar o ingresso
if (idadeUsuario >= idadeMinima) {
    console.log("Compra permitida: O usuário tem idade suficiente.");
} else {
    console.log("Compra negada: O usuário não tem idade suficiente.");
}
