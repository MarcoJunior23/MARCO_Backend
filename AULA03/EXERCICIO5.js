// Crie uma variável com uma string
let textoOriginal = "JavaScript é muito divertido!";

// Converta a string para maiúsculas
let textoMaiusculas = textoOriginal.toUpperCase();

// Converta a string para minúsculas
let textoMinusculas = textoOriginal.toLowerCase();

// Extraia uma parte da string usando slice
let textoFatiado = textoOriginal.slice(0, 10); // Extrai os primeiros 10 caracteres

// Exiba os resultados finais no console
console.log("Texto original:", textoOriginal);
console.log("Texto em maiúsculas:", textoMaiusculas);
console.log("Texto em minúsculas:", textoMinusculas);
console.log("Texto fatiado (primeiros 10 caracteres):", textoFatiado);
