// Declare uma variável numérica
let numero = 123;

// Declare uma variável string
let texto = "456";

// Converta a variável numérica para string
let numeroComoString = numero.toString();

// Converta a variável string para número
let textoComoNumero = Number(texto);

// Exiba os tipos de dados resultantes no console
console.log("Tipo de dado da variável numérica original:", typeof numero);
console.log("Tipo de dado da variável como string:", typeof numeroComoString);
console.log("Tipo de dado da variável string original:", typeof texto);
console.log("Tipo de dado da variável como número:", typeof textoComoNumero);
